    void BackScreen::CopyPart(int startX, int startY, int partType)
    {
        if (partType == 1) {
            for (int y = 0; y < 23; y++) {
                for (int x = 0; x < 40; x++) {
                    // Bounds check for safety (JP: 安全のための範囲チェック)
                    if (startY + y < MAP_HEIGHT && startX + x < MAP_WIDTH) {
                        m_stageMap[startY + y][startX + x] = statgeaPatern_1[y][x];
                    }
                }
            }
        }
    }

